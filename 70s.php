                    </p>

                    <p>Across the country comedy clubs sprung up, offering audiences multiple shows every night.  Not only did the proliferation of comedy clubs expose audiences to new comedians, but they also provided new communities for the comics themselves. Stand-up comedians could make 
                    connections with each other; they could see other acts every night and "workshop" their own material.
                    </p>

                    <h3 class="color-c open-box">Perhaps one of the most iconic club comedians, George Carlin pioneered stand-up in the 70s and remains, to this day, a legend in stand-up history.
                    </h3>

